def auto_convert(rank):
    """第一選択（xxx）→ xxx に適する、等の自動変換"""
    if not rank:
        return rank
    # 括弧内を抽出
    m = re.search(r'[（(](.+?)[）)]', rank)
    content = m.group(1) if m else ''
    if '第一選択' in rank or '第一選択' in rank:
        return (content + 'に適する') if content else 'その疾患の標準的な選択肢'
    if '第二選択' in rank or '第二選択' in rank:
        return (content + 'の場合に代替として検討') if content else '第一選択が使えない際の代替薬'
    if '補助的選択' in rank or '補助療法' in rank:
        return (content + 'を目的に補助的に使用') if content else '補助的な位置付け'
    if '治療抵抗性' in rank:
        return '複数剤が無効な治療抵抗性例に限定'
    # そのまま返す（すでに変換済みなど）
    return rank

def convert(name, rank):
    if name in MANUAL:
        return MANUAL[name]
    return auto_convert(rank)

