'use strict';
const fs = require('fs');

function reorderCategory(drugs, nameOrder) {
  const ordered = [];
  nameOrder.forEach(n => {
    const d = drugs.find(x => x.name === n);
    if (d) ordered.push(d);
    else console.warn('  [警告] 見つからない薬剤名:', n);
  });
  drugs.forEach(d => { if (!ordered.includes(d)) { console.warn('  [未指定] 末尾追加:', d.name); ordered.push(d); } });
  return ordered;
}

function reorderFile(filePath, orderMap) {
  const data = JSON.parse(fs.readFileSync(filePath, 'utf-8'));
  const result = [];
  [...new Set(data.map(x => x.category))].forEach(cat => {
    const catDrugs = data.filter(x => x.category === cat);
    const reordered = orderMap[cat] ? reorderCategory(catDrugs, orderMap[cat]) : catDrugs;
    result.push(...reordered);
  });
  fs.writeFileSync(filePath, JSON.stringify(result, null, 2), 'utf-8');
  console.log(filePath + ' 完了 (' + result.length + '件)');
}

