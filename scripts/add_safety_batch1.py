done = {"lifestyle.json","pain.json","sleep_anxiety.json"}
for fname in sorted(os.listdir("data")):
    if not fname.endswith(".json") or fname in done: continue
    fpath = f"data/{fname}"
    data = json.load(open(fpath, encoding="utf-8"))
    if not isinstance(data, list): continue
    updated = 0
    for drug in data:
        if not isinstance(drug, dict): continue
        name = drug.get("name","")
        if name in ALL_SAFETY and "pregnancy" not in drug:
            p,l,r,d = ALL_SAFETY[name]
            drug["pregnancy"]=p; drug["lactation"]=l
            drug["renal_gfr"]=r; drug["dialysis"]=d
            updated += 1
    if updated > 0:
        with open(fpath, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        print(f"  {fname}: {updated}件")
